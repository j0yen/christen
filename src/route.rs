#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn render_dropin_pure_no_fs_calls() {
        // render_dropin must not touch the filesystem — we call it and assert
        // the returned value is correct without creating any temp dir.
        let dir = Path::new("/tmp/fake-systemd");
        let dropin = render_dropin(
            dir,
            "claude-build.service",
            "/home/jsy/.local/bin/claude-build-headless.sh",
            "/build",
            "wall=7200s,fork=2000",
        );
        assert_eq!(
            dropin.path,
            dir.join("claude-build.service.d/10-christen.conf"),
        );
        // Must contain the clear-then-set pair in order.
        let lines: Vec<&str> = dropin.contents.lines().collect();
        let clear_pos = lines.iter().position(|l| *l == "ExecStart=");
        let set_pos = lines
            .iter()
            .position(|l| l.starts_with("ExecStart=/home/jsy/.local/bin/agentns-claude"));
        assert!(clear_pos.is_some(), "must have clear ExecStart= line");
        assert!(set_pos.is_some(), "must have set ExecStart= line");
        assert!(
            clear_pos.unwrap() < set_pos.unwrap(),
            "clear must come before set"
        );
    }

    #[test]
    fn render_dropin_contains_intent_and_budget() {
        let dropin = render_dropin(
            Path::new("/tmp/sd"),
            "claude-dream.service",
            "/home/jsy/.local/bin/claude-dream-headless.sh",
            "/dream",
            "wall=3600s,fork=500",
        );
        assert!(dropin.contents.contains("--intent /dream"));
        assert!(dropin.contents.contains("--budget wall=3600s,fork=500"));
        assert!(dropin.contents.contains("claude-dream-headless.sh"));
    }
}
